import csv

# Define a lista de domínios migrados
DOMINIOS_MIGRADOS = set(DOMINIOS_MIGRADOS_ALL)

# Define o nome do arquivo CSV de entrada e saída
ARQUIVO_ENTRADA = ".\\data\\migracao_dns.csv"
ARQUIVO_SAIDA = ".\\data\\dados_saida.csv"

# Abre o arquivo CSV de entrada para leitura
with open(ARQUIVO_ENTRADA, newline="") as arquivo_entrada:

    # Cria um leitor de CSV para o arquivo de entrada
    leitor_csv = csv.reader(arquivo_entrada)

    # Abre o arquivo CSV de saída para escrita
    with open(ARQUIVO_SAIDA, "w", newline="") as arquivo_saida:

        # Cria um escritor de CSV para o arquivo de saída
        escritor_csv = csv.writer(arquivo_saida)

        # Itera sobre as linhas do arquivo de entrada
        for linha in leitor_csv:

            # Verifica se o conteúdo da primeira coluna está na lista de domínios migrados
            if linha[0] in DOMINIOS_MIGRADOS:

                # Se estiver, adiciona uma nova coluna com o valor 'MIGRADO'
                linha.append("MIGRADO")

            else:

                # Caso contrário, adiciona uma nova coluna com o valor 'NAO MIGRADO'
                linha.append("NAO MIGRADO")

            # Adiciona o cabeçalho 'STATUS' na primeira linha do arquivo, se ainda não foi adicionado
            if leitor_csv.line_num == 1 and "STATUS" not in linha:
                linha.append("STATUS")

            # Escreve a linha atual no arquivo de saída
            escritor_csv.writerow(linha)
